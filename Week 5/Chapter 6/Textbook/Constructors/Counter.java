public class Counter {
    // Fields
    private int counter;

    // Constructors
    public Counter(int counter) {
        this.counter = counter;
    }

    // Getters
    public int getValue() {
        return this.counter;
    }

    // Setters


    // Methods
    public void increment() {
        this.counter++;
    }

    public void decrement() {
        this.counter--;
    }
}
